/**
 * Formats a phone number by separating the ISD code from the local number
 * Uses known country codes to properly split the number
 * 
 * @param phoneNumber - Full phone number like "+97471157233"
 * @returns Formatted phone number like "+974 71157233"
 */
export const formatPhoneWithCode = (phoneNumber: string | null | undefined): string => {
  if (!phoneNumber) return '';
  
  // Known country code patterns sorted by length (longest first to match correctly)
  const knownCodes = [
    // 4-digit codes
    '+1242', '+1246', '+1264', '+1268', '+1284', '+1340', '+1345', '+1441', '+1473',
    '+1649', '+1664', '+1670', '+1671', '+1684', '+1721', '+1758', '+1767', '+1784',
    '+1787', '+1809', '+1829', '+1849', '+1868', '+1869', '+1876', '+1939',
    // 3-digit codes (Gulf/Middle East)
    '+974', '+971', '+966', '+965', '+968', '+973', '+962', '+961', '+963', '+964',
    '+967', '+970', '+972', '+212', '+213', '+216', '+218', '+249', '+251', '+252',
    '+253', '+254', '+255', '+256', '+257', '+258', '+260', '+261', '+262', '+263',
    '+264', '+265', '+266', '+267', '+268', '+269', '+297', '+298', '+299',
    '+350', '+351', '+352', '+353', '+354', '+355', '+356', '+357', '+358', '+359',
    '+370', '+371', '+372', '+373', '+374', '+375', '+376', '+377', '+378', '+380',
    '+381', '+382', '+383', '+385', '+386', '+387', '+389',
    '+420', '+421', '+423',
    '+500', '+501', '+502', '+503', '+504', '+505', '+506', '+507', '+508', '+509',
    '+590', '+591', '+592', '+593', '+594', '+595', '+596', '+597', '+598', '+599',
    '+670', '+672', '+673', '+674', '+675', '+676', '+677', '+678', '+679',
    '+680', '+681', '+682', '+683', '+685', '+686', '+687', '+688', '+689', '+690',
    '+691', '+692',
    '+850', '+852', '+853', '+855', '+856',
    '+880', '+886',
    '+960', '+975', '+976', '+977', '+992', '+993', '+994', '+995', '+996', '+998',
    // 2-digit codes (common)
    '+93', '+94', '+95', '+98',
    '+20', '+27', '+30', '+31', '+32', '+33', '+34', '+36', '+39',
    '+40', '+41', '+43', '+44', '+45', '+46', '+47', '+48', '+49',
    '+51', '+52', '+53', '+54', '+55', '+56', '+57', '+58', '+60', '+61', '+62',
    '+63', '+64', '+65', '+66', '+81', '+82', '+84', '+86', '+90', '+91', '+92',
    // 1-digit codes
    '+1', '+7',
  ];
  
  for (const code of knownCodes) {
    if (phoneNumber.startsWith(code)) {
      const localNumber = phoneNumber.slice(code.length);
      return `${code} ${localNumber}`;
    }
  }
  
  // Fallback: assume 3-digit country code after +
  if (phoneNumber.startsWith('+') && phoneNumber.length > 4) {
    return `${phoneNumber.slice(0, 4)} ${phoneNumber.slice(4)}`;
  }
  
  return phoneNumber;
};
